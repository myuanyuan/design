/** 
 * 设计原则
*/
/**
 * 单一职责原则  Single responsibility principle
 * 一个程序只做好一件事
 * 如果功能特别复杂就进行拆分
 */

 /**
 * 开放封闭原则 Open close principle
 * 对扩展开发，对修改关闭
 * 新增需求时，扩展新代码，而不是要修改老代码
 * 也是设计的终极目标
 */


/**
 * Interface
 * 接口隔离原则 Interface Segregation Principle
 * 保持接口的单一独立
 * 类似于单一职责，更关注接口 ts中有接口
*/

/**
 * 依赖倒置原则 Dependence inversion principle
 * 面向接口编程 依赖抽象而不依赖具体实现
 * 使用方法只关注接口而不关注具体类的实现、
 * js中使用较少 （没有接口,弱类型）
*/






